function main() {
	print("sin(PI) == ", Math.sin(Math.PI));
	print("cos(PI) == ", Math.cos(Math.PI));
	print("tan(PI) == ", Math.tan(Math.PI));
	print("floor(1.5) == ", Math.floor(1.5));
	print("abs(-5) == ", Math.abs(-5));
	print("sqrt(4) == ", Math.sqrt(4));
	print("pow(3, 2) == ", Math.pow(3, 2));
    return 0;
}
